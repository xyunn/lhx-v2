function profile(id, title, riskLevel, scenarios, techBase, roles, systems, ppe, resources, workControl, abnormal) {
  return {
    id,
    title,
    riskLevel,
    scenarios,
    summary: `适用于${scenarios}等现场，重点控制${abnormal}等风险征兆。`,
    techBase,
    roles,
    systems,
    ppe,
    resources,
    workControl,
    abnormal
  };
}

function createModule(item) {
  return {
    id: item.id,
    title: item.title,
    type: 'module',
    riskLevel: item.riskLevel,
    summary: item.summary,
    children: stages.map((stage) => createStage(item, stage))
  };
}

function createStage(item, stage) {
  return {
    id: `${item.id}-${slug(stage)}`,
    title: stage,
    type: 'stage',
    riskLevel: item.riskLevel,
    summary: stageSummary(item, stage),
    children: aspects.map((aspect) => createAspect(item, stage, aspect))
  };
}

function createAspect(item, stage, aspect) {
  return {
    id: `${item.id}-${slug(stage)}-${slug(aspect)}`,
    title: aspect,
    type: 'aspect',
    moduleTitle: item.title,
    riskLevel: item.riskLevel,
    scenarios: item.scenarios,
    responsible: responsibleOf(aspect),
    stage,
    aspect,
    summary: `${item.title}在${stage}阶段的${aspect}。`,
    content: contentOf(item, stage, aspect),
    checkPoints: checkPointsOf(item, stage, aspect)
  };
}

