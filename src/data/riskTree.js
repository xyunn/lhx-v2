function createModule(profile, index) {
  return {
    id: profile.id,
    title: profile.title,
    type: 'module',
    summary: profile.summary,
    children: stages.map((stage) => createStage(profile, stage, index))
  };
}

function createStage(profile, stage, moduleIndex) {
  return {
    id: `${profile.id}-${slug(stage)}`,
    title: stage,
    type: 'stage',
    summary: stageSummary(profile, stage),
    children: aspects.map((aspect) => createAspect(profile, stage, aspect, moduleIndex))
  };
}

function createAspect(profile, stage, aspect, moduleIndex) {
  return {
    id: `${profile.id}-${slug(stage)}-${slug(aspect)}`,
    title: aspect,
    type: 'aspect',
    moduleTitle: profile.title,
    stage,
    aspect,
    summary: `${profile.title}在${stage}阶段的${aspect}。`,
    content: contentOf(profile, stage, aspect)
  };
}

